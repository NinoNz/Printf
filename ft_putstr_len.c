#include "ft_printf.h"

void	ft_putstr_len(char *s, int *len)
{
	int i;

	i = 0;
	if (!s)
	{
		*len += write(1, "(null)", 6);
	}
	else
	{
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i++;
			(*len)++;
		}
	}
}